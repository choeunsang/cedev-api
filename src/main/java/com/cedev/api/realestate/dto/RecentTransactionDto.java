package com.cedev.api.realestate.dto;

public class RecentTransactionDto {

    private String city;
    private String sido;
    private String sigungu;
    private String dong;
    private String dangi;
    private String dediArea;
    private String contDate;
    private String amount;
    private String floor;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getSido() {
        return sido;
    }

    public void setSido(String sido) {
        this.sido = sido;
    }

    public String getSigungu() {
        return sigungu;
    }

    public void setSigungu(String sigungu) {
        this.sigungu = sigungu;
    }

    public String getDong() {
        return dong;
    }

    public void setDong(String dong) {
        this.dong = dong;
    }

    public String getDangi() {
        return dangi;
    }

    public void setDangi(String dangi) {
        this.dangi = dangi;
    }

    public String getDediArea() {
        return dediArea;
    }

    public void setDediArea(String dediArea) {
        this.dediArea = dediArea;
    }

    public String getContDate() {
        return contDate;
    }

    public void setContDate(String contDate) {
        this.contDate = contDate;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getFloor() {
        return floor;
    }

    public void setFloor(String floor) {
        this.floor = floor;
    }
}